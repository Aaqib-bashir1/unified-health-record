def list_patient_visits(user, patient_id: UUID):
    """List all visits for a patient. Requires manage access."""
    access = _get_active_access(user, patient_id)
    if not access.can_manage_access:
        raise AccessDenied("Only the primary holder or managing delegate can view visits.")

    return (
        PatientVisit.objects
        .filter(patient_id=patient_id)
        .select_related("organisation")
        .order_by("-initiated_at")
    )


# ===========================================================================
# LAZY VISIT ACCESS CHECK (called by practitioner-facing endpoints)
# ===========================================================================

def check_practitioner_visit_access(practitioner, patient_id: UUID) -> PatientVisit:
    """
    Check whether a practitioner has visit-based access to a patient.

    Called by any endpoint that serves data to a practitioner during a visit.
    If the practitioner has access, creates a PatientVisitAccess record lazily
    (on first call) or updates last_accessed_at (on subsequent calls).

    Returns the active PatientVisit if access is granted.
    Raises AccessDenied if no active visit exists for this org.

    This is the core of the lazy access model:
      - No records are created at visit initiation
      - Records are created here, on first actual data access
      - The audit log reflects who actually looked at the data
    """
    # Find an active visit for this patient at the practitioner's organisation
    visit = PatientVisit.objects.filter(
        patient_id=patient_id,
        organisation=practitioner.organisation,
        is_active=True,
        expires_at__gt=timezone.now(),
    ).first()

    if not visit:
        raise PractitionerNotAtOrg()

    # Lazy create or update the visit access record
    _get_or_create_visit_access(visit, practitioner)

    return visit


def _get_or_create_visit_access(
    visit: PatientVisit,
    practitioner,
) -> PatientVisitAccess:
    """
    Get or create a PatientVisitAccess record for this (visit, practitioner) pair.

    Uses get_or_create to handle concurrent first-access requests from the
    same practitioner safely. The unique constraint on (visit, practitioner)
    prevents duplicate records even under concurrent requests.

    On subsequent calls (practitioner already has a record):
      - Updates last_accessed_at via auto_now=True on save()
      - Does not create a new record
    """
    access, created = PatientVisitAccess.objects.get_or_create(
        visit=visit,
        practitioner=practitioner,
        defaults={
            "patient":  visit.patient,
            "is_active": True,
        },
    )

    if not created:
        # Record exists — touch it to update last_accessed_at
        access.save(update_fields=["last_accessed_at"])

    if created:
        logger.info(
            "Lazy visit access created. visit_id=%s practitioner_id=%s patient_id=%s",
            visit.id, practitioner.id, visit.patient_id,
        )

    return access


