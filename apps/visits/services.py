_ORG_QR_TOKEN_TYPE          = "org_qr"


# ===========================================================================
# ORG QR TOKEN VERIFICATION
# ===========================================================================

def _verify_org_qr_token(token: str) -> UUID:
    """
    Decode and verify a signed org QR token.
    Returns the organisation_id encoded in the token.

    Org QR tokens are signed with SECRET_KEY using HS256.
    They encode:
        {
            "type":   "org_qr",
            "org_id": "<uuid>",
            "exp":    <unix timestamp>   (15 min default, set when generated)
        }

    Raises ValueError if the token is invalid, expired, or wrong type.
    """
    try:
        payload = jwt.decode(token, settings.SECRET_KEY, algorithms=["HS256"])
    except jwt.ExpiredSignatureError:
        raise InvalidOrgQRToken("Organisation QR code has expired. Ask reception to refresh it.")
    except jwt.InvalidTokenError:
        raise InvalidOrgQRToken("Invalid organisation QR code.")

    if payload.get("type") != _ORG_QR_TOKEN_TYPE:
        raise InvalidOrgQRToken("Invalid QR code type.")

    try:
        return UUID(payload["org_id"])
    except (KeyError, ValueError):
        raise InvalidOrgQRToken("Malformed organisation QR code.")


