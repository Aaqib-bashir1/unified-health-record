@transaction.atomic
def end_visit(user, patient_id: UUID, visit_id: UUID) -> PatientVisit:
    """
    Patient (or org admin) ends a visit early.
    Deactivates the visit and all associated PatientVisitAccess records.
    """
    # Verify the user has manage access to this patient
    access = _get_active_access(user, patient_id)
    if not access.can_manage_access:
        raise AccessDenied("Only the primary holder or managing delegate can end a visit.")

    try:
        visit = PatientVisit.objects.select_for_update().get(
            id=visit_id,
            patient_id=patient_id,
        )
    except PatientVisit.DoesNotExist:
        raise VisitNotFound()

    if not visit.is_active:
        raise VisitAlreadyEnded()

    now = timezone.now()
    visit.is_active = False
    visit.ended_at  = now
    visit.ended_by  = user
    visit.save(update_fields=["is_active", "ended_at", "ended_by", "updated_at"])

    # Deactivate all practitioner visit access records derived from this visit
    PatientVisitAccess.objects.filter(
        visit=visit,
        is_active=True,
    ).update(is_active=False, revoked_at=now)

    logger.info(
        "Visit ended. visit_id=%s patient_id=%s ended_by=%s",
        visit_id, patient_id, user.id,
    )

    return visit


