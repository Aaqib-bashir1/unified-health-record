# INITIATE VISIT
# ===========================================================================

@transaction.atomic
def initiate_visit(user, patient_id: UUID, org_qr_token: str, data=None) -> PatientVisit:
    """
    Patient initiates a visit by scanning an organisation's QR code.

    Steps:
      1. Verify the org QR token → extract org_id
      2. Verify patient exists and requesting user has primary/delegate access
      3. Check for an already-active visit at this org (prevent duplicates)
      4. Create PatientVisit — no PatientVisitAccess records yet (lazy)

    The QR token is a signed JWT generated by the org's QR endpoint.
    It encodes the org_id and has a short expiry (15 min).
    The patient scans it at reception.
    """
    # Verify the org QR token
    try:
        org_id = _verify_org_qr_token(org_qr_token)
    except InvalidOrgQRToken as e:
        raise e

    # Verify patient access — patient must have manage rights to initiate a visit
    # (they are presenting their own records to the org)
    access = _get_active_access(user, patient_id)
    if not access.can_manage_access:
        raise AccessDenied(
            "Only the primary holder or managing delegate can initiate a visit."
        )

    patient = access.patient
    if not patient.is_active:
        raise PatientRetracted()

    # Verify the organisation exists and is verified
    try:
        from django.apps import apps
        Organisation = apps.get_model("integrations", "Organisation")
        organisation = Organisation.objects.get(pk=org_id, verified=True)
    except LookupError:
        raise OrganisationNotFound("integrations app not yet configured.")
    except Organisation.DoesNotExist:
        raise OrganisationNotFound()

    # Prevent duplicate active visits at the same org
    existing = PatientVisit.objects.filter(
        patient=patient,
        organisation=organisation,
        is_active=True,
        expires_at__gt=timezone.now(),
    ).first()

    if existing:
        raise VisitAlreadyActive(
            f"You already have an active visit at this organisation "
            f"(expires {existing.expires_at.strftime('%H:%M %Z')}). "
            "End the existing visit first."
        )

    expiry_hours = _VISIT_DEFAULT_EXPIRY_HOURS
    if data and hasattr(data, "expiry_hours") and data.expiry_hours:
        expiry_hours = min(data.expiry_hours, 72)  # Hard cap: 3 days

    visit = PatientVisit.objects.create(
        patient       = patient,
        organisation  = organisation,
        initiated_by  = user,
        expires_at    = timezone.now() + timedelta(hours=expiry_hours),
        visit_reason  = getattr(data, "visit_reason", None),
    )

    logger.info(
        "Visit initiated. visit_id=%s patient_id=%s org_id=%s expiry_hours=%s",
        visit.id, patient_id, org_id, expiry_hours,
    )

    return visit


